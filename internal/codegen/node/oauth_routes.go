package node

import (
	"fmt"
	"strings"

	"github.com/barun-bash/human/internal/ir"
)

// generateOAuthRoutes produces OAuth authentication route handlers.
func generateOAuthRoutes(app *ir.Application) string {
	var b strings.Builder

	b.WriteString("// Generated by Human compiler — do not edit\n\n")
	b.WriteString("import { Router, Request, Response } from 'express';\n")
	b.WriteString("import passport from 'passport';\n")
	b.WriteString("import { signToken } from '../middleware/auth';\n")

	// Import configure functions for each OAuth provider
	for _, integ := range app.Integrations {
		if integ.Type == "oauth" {
			fmt.Fprintf(&b, "import { configure%sAuth } from '../services/oauth';\n", integ.Service)
		}
	}

	b.WriteString("\nconst router = Router();\n\n")

	// Configure each OAuth provider
	for _, integ := range app.Integrations {
		if integ.Type == "oauth" {
			provider := strings.ToLower(integ.Service)
			callbackURL := "/auth/" + provider + "/callback"
			if v, ok := integ.Config["callback_url"]; ok {
				callbackURL = v
			}

			fmt.Fprintf(&b, "// Configure %s OAuth\n", integ.Service)
			fmt.Fprintf(&b, "configure%sAuth('%s');\n\n", integ.Service, callbackURL)

			// Auth initiation route
			scopes := "['email', 'profile']"
			if strings.Contains(provider, "github") {
				scopes = "['user:email']"
			}
			fmt.Fprintf(&b, "router.get('/%s', passport.authenticate('%s', { scope: %s }));\n\n", provider, provider, scopes)

			// Callback route
			fmt.Fprintf(&b, "router.get('/%s/callback',\n", provider)
			fmt.Fprintf(&b, "  passport.authenticate('%s', { session: false, failureRedirect: '/login' }),\n", provider)
			b.WriteString("  (req: Request, res: Response) => {\n")
			b.WriteString("    const user = req.user as any;\n")
			b.WriteString("    const token = signToken(user.id, user.role || 'user');\n")
			b.WriteString("    res.redirect(`/?token=${token}`);\n")
			b.WriteString("  },\n")
			b.WriteString(");\n\n")
		}
	}

	b.WriteString("export { router };\n")
	return b.String()
}

// hasOAuthIntegration returns true if any OAuth integration exists.
func hasOAuthIntegration(app *ir.Application) bool {
	for _, integ := range app.Integrations {
		if integ.Type == "oauth" {
			return true
		}
	}
	return false
}
