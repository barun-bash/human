// generateOAuthService produces a Go OAuth2 service.
func generateOAuthService(_ string, integ *ir.Integration) string {
	var b strings.Builder

	b.WriteString("// Generated by Human compiler — do not edit\n")
	fmt.Fprintf(&b, "// Integration: %s (oauth)\n", integ.Service)
	b.WriteString("package services\n\n")

	b.WriteString("import \"golang.org/x/oauth2\"\n\n")

	// Determine credential env vars.
	clientIDEnv := strings.ToUpper(strings.ReplaceAll(integ.Service, " ", "_")) + "_CLIENT_ID"
	clientSecretEnv := strings.ToUpper(strings.ReplaceAll(integ.Service, " ", "_")) + "_CLIENT_SECRET"
	for key, envVar := range integ.Credentials {
		lower := strings.ToLower(key)
		if strings.Contains(lower, "secret") {
			clientSecretEnv = envVar
		} else if strings.Contains(lower, "id") || strings.Contains(lower, "client") {
			clientIDEnv = envVar
		}
	}

	// Determine OAuth endpoints based on provider.
	serviceLower := strings.ToLower(integ.Service)
	authURL := "https://accounts.google.com/o/oauth2/auth"
	tokenURL := "https://oauth2.googleapis.com/token"
	if strings.Contains(serviceLower, "github") {
		authURL = "https://github.com/login/oauth/authorize"
		tokenURL = "https://github.com/login/oauth/access_token"
	} else if strings.Contains(serviceLower, "facebook") {
		authURL = "https://www.facebook.com/v18.0/dialog/oauth"
		tokenURL = "https://graph.facebook.com/v18.0/oauth/access_token"
	}

	callbackURL := "/auth/callback"
	if v, ok := integ.Config["callback_url"]; ok {
		callbackURL = v
	}

	b.WriteString("// GetOAuth2Config returns an OAuth2 configuration for the given provider.\n")
	b.WriteString("func GetOAuth2Config() *oauth2.Config {\n")
	b.WriteString("\treturn &oauth2.Config{\n")
	fmt.Fprintf(&b, "\t\tClientID:     os.Getenv(\"%s\"),\n", clientIDEnv)
	fmt.Fprintf(&b, "\t\tClientSecret: os.Getenv(\"%s\"),\n", clientSecretEnv)
	fmt.Fprintf(&b, "\t\tRedirectURL:  \"%s\",\n", callbackURL)
	b.WriteString("\t\tEndpoint: oauth2.Endpoint{\n")
	fmt.Fprintf(&b, "\t\t\tAuthURL:  \"%s\",\n", authURL)
	fmt.Fprintf(&b, "\t\t\tTokenURL: \"%s\",\n", tokenURL)
	b.WriteString("\t\t},\n")
	b.WriteString("\t}\n")
	b.WriteString("}\n")

	return b.String()
}

// generateGenericGoService produces a minimal Go service for unknown integrations.
func generateGenericGoService(_ string, integ *ir.Integration) string {
	var b strings.Builder

	b.WriteString("// Generated by Human compiler — do not edit\n")
	fmt.Fprintf(&b, "// Integration: %s\n", integ.Service)
	b.WriteString("package services\n\n")

	structName := toPascalCase(integ.Service) + "Service"

	fmt.Fprintf(&b, "// %s provides access to the %s integration.\n", structName, integ.Service)
	fmt.Fprintf(&b, "type %s struct {\n", structName)
	for key, envVar := range integ.Credentials {
		fieldName := toPascalCase(key)
		fmt.Fprintf(&b, "\t%s string // from env: %s\n", fieldName, envVar)
	}
	for key, val := range integ.Config {
		fieldName := toPascalCase(key)
		fmt.Fprintf(&b, "\t%s string // default: %s\n", fieldName, val)
	}
	b.WriteString("}\n\n")

	fmt.Fprintf(&b, "// New%s creates a new %s with configuration from environment.\n", structName, structName)
	fmt.Fprintf(&b, "func New%s() *%s {\n", structName, structName)
	fmt.Fprintf(&b, "\treturn &%s{}\n", structName)
	b.WriteString("}\n")

	return b.String()
}
