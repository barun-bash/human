// generateIntegrations returns a map of file paths (relative to outputDir) to
// their generated Go content for all integration services.
func generateIntegrations(moduleName string, app *ir.Application) map[string]string {
	if len(app.Integrations) == 0 {
		return nil
	}

	files := make(map[string]string)

	for _, integ := range app.Integrations {
		switch integ.Type {
		case "email":
			files["services/email.go"] = generateEmailService(moduleName, integ)
		case "storage":
			files["services/storage.go"] = generateStorageService(moduleName, integ)
		case "payment":
			files["services/stripe.go"] = generatePaymentService(moduleName, integ)
		case "messaging":
			files["services/slack.go"] = generateMessagingService(moduleName, integ)
		case "oauth":
			files["services/oauth.go"] = generateOAuthService(moduleName, integ)
		default:
			files["services/"+toSnakeCase(integ.Service)+".go"] = generateGenericGoService(moduleName, integ)
		}
	}

	return files
}

// generateEmailService produces a Go email service using SendGrid.
func generateEmailService(_ string, integ *ir.Integration) string {
	var b strings.Builder

	b.WriteString("// Generated by Human compiler — do not edit\n")
	fmt.Fprintf(&b, "// Integration: %s (email)\n", integ.Service)
	b.WriteString("package services\n\n")

	b.WriteString("import (\n")
	b.WriteString("\t\"os\"\n\n")
	b.WriteString("\t\"github.com/sendgrid/sendgrid-go\"\n")
	b.WriteString("\t\"github.com/sendgrid/sendgrid-go/helpers/mail\"\n")
	b.WriteString(")\n\n")

	// Determine the API key env var.
	apiKeyEnv := "SENDGRID_API_KEY"
	for _, envVar := range integ.Credentials {
		apiKeyEnv = envVar
		break
	}

	// Sender email.
	senderEmail := "noreply@example.com"
	if v, ok := integ.Config["sender_email"]; ok {
		senderEmail = v
	}

	b.WriteString("// SendEmail sends an email via SendGrid.\n")
	b.WriteString("func SendEmail(to, subject, textContent string) error {\n")
	fmt.Fprintf(&b, "\tfrom := mail.NewEmail(\"App\", os.Getenv(\"%s\"))\n", "SENDER_EMAIL")
	if senderEmail != "noreply@example.com" {
		// If a custom sender is configured, use it as fallback.
		b.WriteString("\tif from.Address == \"\" {\n")
		fmt.Fprintf(&b, "\t\tfrom = mail.NewEmail(\"App\", \"%s\")\n", senderEmail)
		b.WriteString("\t}\n")
	}
	b.WriteString("\ttoAddr := mail.NewEmail(\"\", to)\n")
	b.WriteString("\tmessage := mail.NewSingleEmail(from, subject, toAddr, textContent, \"\")\n")
	fmt.Fprintf(&b, "\tclient := sendgrid.NewSendClient(os.Getenv(\"%s\"))\n", apiKeyEnv)
	b.WriteString("\t_, err := client.Send(message)\n")
	b.WriteString("\treturn err\n")
	b.WriteString("}\n")

	return b.String()
}

